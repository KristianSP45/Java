public class Calculo {
    private int a;
    private int b;

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
    
    public void trocarValores() {
        int temp = a;
        a = b;
        b = temp;
    }
}
